const localeState = ref((typeof window !== 'undefined' && localStorage.getItem(STORAGE_KEY)) || FALLBACK)

function getByPath(obj, key) {
  return key.split('.').reduce((acc, k) => (acc && acc[k] !== undefined ? acc[k] : undefined), obj)
}

export function useI18n() {
  const locale = computed({
    get: () => localeState.value,
    set: (v) => {
      localeState.value = v
      if (typeof window !== 'undefined') {
        localStorage.setItem(STORAGE_KEY, v)
      }
    }
  })

  const t = (key, vars = {}) => {
    const table = messages[locale.value] || messages[FALLBACK]
    const fallbackTable = messages[FALLBACK]
    let text = getByPath(table, key)
    if (text === undefined) text = getByPath(fallbackTable, key)
    if (typeof text !== 'string') return key
    return text.replace(/\{(\w+)\}/g, (_, name) => (vars[name] ?? `{${name}}`))
  }

  const isEnglish = computed(() => locale.value === 'en-US')
  const toggleLocale = () => {
    locale.value = locale.value === 'zh-CN' ? 'en-US' : 'zh-CN'
  }

  return { locale, t, isEnglish, toggleLocale }
}
